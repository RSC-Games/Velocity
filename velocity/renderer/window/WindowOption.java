package velocity.renderer.window;

/**
 * Window option constants and hints for window creation. Used for cross-renderer abstraction
 * and window creation.
 */
public enum WindowOption {
    HINT_FULLSCREEN, // Switch on fullscreen.
    HINT_RESIZABLE, // Allow the window to be resized.
    HINT_ALWAYS_ON_TOP, // Force the window to stay on top.
}
